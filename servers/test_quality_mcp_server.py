def _save_history(project_path: str, reports: list[MutationReport]):
    """Save a snapshot of mutation scores for trend tracking."""
    os.makedirs(HISTORY_DIR, exist_ok=True)
    project_name = os.path.basename(project_path)
    ts = datetime.now().strftime("%Y%m%d-%H%M%S")

    overall_killed = sum(r.killed for r in reports)
    overall_survived = sum(r.survived for r in reports)
    denom = overall_killed + overall_survived
    overall_score = round((overall_killed / denom * 100) if denom > 0 else 0, 1)

    entry = {
        "timestamp": datetime.now().isoformat(),
        "project": project_name,
        "overall_mutation_score": overall_score,
        "total_classes": len(reports),
        "total_mutants": sum(r.total_mutants for r in reports),
        "killed": overall_killed,
        "survived": overall_survived,
        "classes_below_threshold": len([
            r for r in reports
            if r.mutation_score < _load_config().get("mutation_score_threshold", 80)
        ]),
    }

    # Append to history file
    history_file = os.path.join(HISTORY_DIR, f"{project_name}.jsonl")
    with open(history_file, "a") as f:
        f.write(json.dumps(entry) + "\n")

    return entry


def _load_history(project_name: str, limit: int = 30) -> list[dict]:
    """Load mutation score history for trend analysis."""
    history_file = os.path.join(HISTORY_DIR, f"{project_name}.jsonl")
    if not os.path.isfile(history_file):
        return []

    entries = []
    try:
        with open(history_file) as f:
            for line in f:
                if line.strip():
                    entries.append(json.loads(line))
    except Exception:
        pass

    return entries[-limit:]


