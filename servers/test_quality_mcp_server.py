@mcp.tool()
def configure_quality_gates(
    mutation_threshold: int = 0,
    line_coverage_threshold: int = 0,
    new_code_threshold: int = 0,
    pit_mutators: str = "",
    exclude_packages: str = "",
) -> dict:
    """
    Configure test quality thresholds and exclusions.

    Args:
        mutation_threshold:      Target mutation score % (default 80)
        line_coverage_threshold: Minimum line coverage % (default 70)
        new_code_threshold:      Mutation threshold for new code (default 90)
        pit_mutators:            PIT mutator group: DEFAULTS, STRONGER, ALL
        exclude_packages:        Comma-separated package patterns to skip
    """
    config = _load_config()

    if mutation_threshold > 0:
        config["mutation_score_threshold"] = mutation_threshold
    if line_coverage_threshold > 0:
        config["line_coverage_threshold"] = line_coverage_threshold
    if new_code_threshold > 0:
        config["new_code_mutation_threshold"] = new_code_threshold
    if pit_mutators:
        config["pit_mutators"] = pit_mutators
    if exclude_packages:
        config["exclude_packages"] = [p.strip() for p in exclude_packages.split(",")]

    _save_config(config)
    return {"status": "configured", "config": config}


@mcp.tool()
def analyze_mutation_coverage(
    project_path: str,
    run_pit: bool = False,
    target_class: str = "",
) -> dict:
    """
    Analyze mutation testing results for a project.

    If run_pit=True, executes PIT first. Otherwise, parses the most
    recent PIT report from the project's target/build directory.

    Args:
        project_path:  Path to the Maven/Gradle project.
        run_pit:       Whether to run PIT first (takes minutes).
        target_class:  Specific class pattern to test (e.g., "com.company.order.*")

    Returns:
        Per-class mutation scores, risk assessment, and prioritized test gaps.
    """
    project_path = os.path.abspath(project_path)
    config = _load_config()

    # Run PIT if requested
    if run_pit:
        pit_result = _run_pit(project_path, target_class, config)
        if not pit_result["success"]:
            return {
                "error": "PIT execution failed",
                "details": pit_result.get("stderr", ""),
                "hint": "Ensure pitest-maven plugin is in pom.xml and tests compile.",
            }

    # Find and parse report
    report_path = _find_pit_report(project_path)
    if not report_path:
        return {
            "error": "No PIT report found.",
            "hint": "Run with run_pit=True, or execute PIT manually: mvn pitest:mutationCoverage",
        }

    reports = _parse_pit_xml_report(report_path)
    if not reports:
        return {"error": "PIT report was empty or unparsable."}

    # Filter exclusions
    excluded_pkgs = config.get("exclude_packages", [])
    filtered = []
    for r in reports:
        skip = False
        for pattern in excluded_pkgs:
            regex = pattern.replace(".", "\\.").replace("*", ".*")
            if re.match(regex, r.class_fqcn):
                skip = True
                break
        if not skip:
            filtered.append(r)

    threshold = config.get("mutation_score_threshold", 80)

    # Score risk for each class
    project_name = os.path.basename(project_path)
    class_results = []
    below_threshold = []

    for r in filtered:
        # Find source file
        source_path = ""
        simple_name = r.class_fqcn.rsplit(".", 1)[-1]
        for sf in Path(project_path).rglob(f"{simple_name}.java"):
            if "/test/" not in str(sf):
                source_path = str(sf)
                break

        risk = _score_class_risk(r.class_fqcn, source_path, config)
        gaps = _analyze_test_gaps(r, risk, config)

        entry = {
            "class": r.class_fqcn,
            "mutation_score": r.mutation_score,
            "killed": r.killed,
            "survived": r.survived,
            "no_coverage": r.no_coverage,
            "total_mutants": r.total_mutants,
            "meets_threshold": r.mutation_score >= threshold,
            "risk_category": risk.category,
            "risk_score": risk.overall_risk,
            "risk_factors": [f["factor"] for f in risk.risk_factors],
            "test_gaps": len(gaps),
            "top_gaps": [
                {"method": g.method_name, "type": g.mutant_type, "line": g.line_number}
                for g in gaps[:5]
            ],
        }
        class_results.append(entry)

        if r.mutation_score < threshold:
            below_threshold.append(entry)

    # Sort by priority: risk × gap size
    below_threshold.sort(
        key=lambda c: -(c["risk_score"] * c["test_gaps"]),
    )

    # Overall stats
    total_killed = sum(r.killed for r in filtered)
    total_survived = sum(r.survived for r in filtered)
    denom = total_killed + total_survived
    overall_score = round((total_killed / denom * 100) if denom > 0 else 0, 1)

    # Save history
    _save_history(project_path, filtered)

    return {
        "project": project_path,
        "pit_report": report_path,
        "overall_mutation_score": overall_score,
        "threshold": threshold,
        "meets_threshold": overall_score >= threshold,
        "total_classes": len(filtered),
        "classes_below_threshold": len(below_threshold),
        "summary": {
            "total_mutants": sum(r.total_mutants for r in filtered),
            "killed": total_killed,
            "survived": total_survived,
            "no_coverage": sum(r.no_coverage for r in filtered),
        },
        "priority_classes": below_threshold[:15],
        "all_classes": sorted(class_results, key=lambda c: c["mutation_score"]),
    }


@mcp.tool()
def generate_tests_for_gaps(
    project_path: str,
    class_fqcn: str = "",
    max_classes: int = 5,
    output_dir: str = "",
) -> dict:
    """
    Generate meaningful test classes targeting specific mutation gaps.

    Tests are generated to KILL SURVIVING MUTANTS, not to pad coverage.
    Each test method targets a specific mutant type with an appropriate
    assertion strategy.

    Args:
        project_path:  Project root (to find PIT report and source files)
        class_fqcn:    Specific class to generate tests for (empty = top priority)
        max_classes:    Maximum number of classes to generate tests for
        output_dir:     Where to write generated tests (default: project test dir)
    """
    project_path = os.path.abspath(project_path)
    config = _load_config()

    report_path = _find_pit_report(project_path)
    if not report_path:
        return {"error": "No PIT report found. Run analyze_mutation_coverage first."}

    reports = _parse_pit_xml_report(report_path)
    threshold = config.get("mutation_score_threshold", 80)

    # Select classes to generate tests for
    targets = []
    for r in reports:
        if class_fqcn and r.class_fqcn != class_fqcn:
            if not r.class_fqcn.endswith(f".{class_fqcn}"):
                continue
        if r.mutation_score >= threshold and not class_fqcn:
            continue
        if not r.surviving_mutants:
            continue

        source_path = ""
        simple_name = r.class_fqcn.rsplit(".", 1)[-1]
        for sf in Path(project_path).rglob(f"{simple_name}.java"):
            if "/test/" not in str(sf):
                source_path = str(sf)
                break

        risk = _score_class_risk(r.class_fqcn, source_path, config)
        gaps = _analyze_test_gaps(r, risk, config)

        targets.append({
            "report": r,
            "risk": risk,
            "gaps": gaps,
            "source_path": source_path,
            "priority": risk.overall_risk * len(gaps),
        })

    targets.sort(key=lambda t: -t["priority"])
    targets = targets[:max_classes]

    if not targets:
        return {"message": "No classes below threshold with surviving mutants."}

    # Determine output directory
    if not output_dir:
        # Standard Maven test directory
        output_dir = os.path.join(project_path, "src", "test", "java")

    generated_files = []
    for t in targets:
        report = t["report"]
        gaps = t["gaps"]
        source_path = t["source_path"]

        test_content = _generate_test_class(
            report.class_fqcn, gaps, source_path, config,
        )

        # Determine output path
        package_path = report.class_fqcn.rsplit(".", 1)[0].replace(".", os.sep) if "." in report.class_fqcn else ""
        simple_name = report.class_fqcn.rsplit(".", 1)[-1]
        test_file_name = f"{simple_name}MutationTest.java"
        test_dir = os.path.join(output_dir, package_path)
        os.makedirs(test_dir, exist_ok=True)
        test_file = os.path.join(test_dir, test_file_name)

        Path(test_file).write_text(test_content)

        generated_files.append({
            "class": report.class_fqcn,
            "file": test_file,
            "gaps_targeted": len(gaps),
            "risk": t["risk"].category,
            "current_mutation_score": report.mutation_score,
        })

    return {
        "generated": len(generated_files),
        "files": generated_files,
        "next_steps": [
            "1. Fill in the TODO sections with actual test logic",
            "2. Run tests: mvn test",
            "3. Re-run PIT: mvn pitest:mutationCoverage",
            "4. Verify mutation score improved: analyze_mutation_coverage()",
        ],
    }


@mcp.tool()
def mutation_score_trend(project_path: str) -> dict:
    """
    Show mutation score trend over time for a project.

    Returns historical data points for charting.
    """
    project_name = os.path.basename(os.path.abspath(project_path))
    history = _load_history(project_name)

    if not history:
        return {"message": f"No history for '{project_name}'. Run analyze_mutation_coverage first."}

    config = _load_config()
    threshold = config.get("mutation_score_threshold", 80)

    # Compute trend
    if len(history) >= 2:
        first = history[0]["overall_mutation_score"]
        latest = history[-1]["overall_mutation_score"]
        trend = round(latest - first, 1)
        trend_direction = "IMPROVING" if trend > 0 else "DECLINING" if trend < 0 else "STABLE"
    else:
        trend = 0
        trend_direction = "INSUFFICIENT_DATA"

    return {
        "project": project_name,
        "threshold": threshold,
        "data_points": len(history),
        "latest_score": history[-1]["overall_mutation_score"] if history else 0,
        "trend": trend,
        "trend_direction": trend_direction,
        "history": history,
    }


@mcp.tool()
def quality_gate_check(project_path: str) -> dict:
    """
    Run a quality gate check suitable for CI/CD pipelines.

    Returns PASS or FAIL with specific reasons.

    This is the tool to call from CI to decide if a PR should be blocked.
    """
    project_path = os.path.abspath(project_path)
    config = _load_config()

    report_path = _find_pit_report(project_path)
    if not report_path:
        return {
            "gate": "SKIP",
            "reason": "No PIT report found. Run PIT first.",
        }

    reports = _parse_pit_xml_report(report_path)
    threshold = config.get("mutation_score_threshold", 80)
    line_threshold = config.get("line_coverage_threshold", 70)

    total_killed = sum(r.killed for r in reports)
    total_survived = sum(r.survived for r in reports)
    denom = total_killed + total_survived
    overall_score = round((total_killed / denom * 100) if denom > 0 else 0, 1)

    failures = []

    # Gate 1: Overall mutation score
    if overall_score < threshold:
        failures.append({
            "gate": "MUTATION_SCORE",
            "actual": overall_score,
            "threshold": threshold,
            "message": f"Overall mutation score {overall_score}% is below {threshold}% threshold.",
        })

    # Gate 2: No class with zero coverage
    zero_coverage = [r for r in reports if r.no_coverage == r.total_mutants and r.total_mutants > 0]
    if zero_coverage:
        failures.append({
            "gate": "ZERO_COVERAGE_CLASSES",
            "count": len(zero_coverage),
            "classes": [r.class_fqcn for r in zero_coverage[:10]],
            "message": f"{len(zero_coverage)} classes have zero test coverage.",
        })

    # Gate 3: High-risk classes must be above threshold
    for r in reports:
        source_path = ""
        simple_name = r.class_fqcn.rsplit(".", 1)[-1]
        for sf in Path(project_path).rglob(f"{simple_name}.java"):
            if "/test/" not in str(sf):
                source_path = str(sf)
                break

        risk = _score_class_risk(r.class_fqcn, source_path, config)
        if risk.category in ("CRITICAL", "HIGH") and r.mutation_score < threshold:
            failures.append({
                "gate": "HIGH_RISK_UNDERTESTED",
                "class": r.class_fqcn,
                "risk": risk.category,
                "score": r.mutation_score,
                "threshold": threshold,
                "message": f"{risk.category}-risk class {r.class_fqcn} has {r.mutation_score}% mutation score.",
            })

    passed = len(failures) == 0

    return {
        "gate": "PASS" if passed else "FAIL",
        "overall_mutation_score": overall_score,
        "threshold": threshold,
        "total_classes": len(reports),
        "failures": failures,
    }


@mcp.tool()
def get_config() -> dict:
    """View current test quality configuration."""
    return _load_config()


