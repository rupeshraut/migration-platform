# Test Gap Analysis
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def _analyze_test_gaps(report: MutationReport, risk: RiskScore, config: dict) -> list[TestGap]:
    """Identify specific test gaps from surviving mutants."""
    gaps = []
    threshold = config.get("mutation_score_threshold", 80)

    for mutant in report.surviving_mutants:
        mutator = mutant.get("mutator", "")
        method = mutant.get("method", "")
        line = mutant.get("line", 0)
        status = mutant.get("status", "")

        # Skip excluded methods
        excluded = config.get("exclude_methods", [])
        if any(method.lower().startswith(e.lower()) for e in excluded):
            continue

        gap_type = "SURVIVING_MUTANT" if status == "SURVIVED" else "NO_COVERAGE"
        suggestion = _suggest_test_for_mutant(report.class_fqcn, method, mutator, line)

        gap = TestGap(
            class_fqcn=report.class_fqcn,
            method_name=method,
            gap_type=gap_type,
            description=f"{mutator} at line {line} in {method}()",
            mutant_type=mutator,
            line_number=line,
            risk_score=risk.overall_risk,
            suggested_test=suggestion,
        )
        gaps.append(gap)

    # Sort: highest risk first, then by gap type (NO_COVERAGE before SURVIVING)
    gaps.sort(key=lambda g: (-g.risk_score, g.gap_type, g.method_name))
    return gaps


def _suggest_test_for_mutant(class_fqcn: str, method: str, mutator: str, line: int) -> str:
    """Generate a test suggestion based on the surviving mutant type."""
    simple_class = class_fqcn.rsplit(".", 1)[-1]
    camel_method = method[0].upper() + method[1:] if method else "Method"

    suggestions = {
        "BOUNDARY": (
            f"Test {method}() with boundary values. The boundary mutation "
            f"(< → <=) survived at line {line}, meaning your test doesn't "
            f"distinguish between < and <=. Add a test with the exact "
            f"boundary value."
        ),
        "NEGATE_COND": (
            f"Test {method}() with both true and false conditions. "
            f"The negated conditional survived at line {line}, meaning "
            f"your test only covers one branch. Add a test for the "
            f"opposite condition."
        ),
        "RETURN_VALUE": (
            f"Assert the EXACT return value of {method}(). A return "
            f"value mutation survived at line {line} — your test calls "
            f"the method but doesn't verify what it returns."
        ),
        "NULL_RETURN": (
            f"Assert that {method}() does NOT return null. A null-return "
            f"mutation survived at line {line}."
        ),
        "VOID_CALL": (
            f"Verify the side effect of a void method call in {method}(). "
            f"PIT removed a void call at line {line} and no test detected it. "
            f"Use Mockito.verify() to check the interaction."
        ),
        "MATH": (
            f"Test {method}() with values that distinguish between "
            f"+, -, *, /. A math mutation survived at line {line}. "
            f"Use values where the operations produce different results "
            f"(avoid 0, 1, 2)."
        ),
        "EMPTY_RETURN": (
            f"Assert the content of the return value from {method}(), "
            f"not just that it's non-null. An empty-return mutation "
            f"survived at line {line}."
        ),
    }

    for key, msg in suggestions.items():
        if key in mutator:
            return msg

    return (
        f"Add a test for {method}() that detects the {mutator} mutation "
        f"at line {line}. The current tests don't catch this change."
    )


