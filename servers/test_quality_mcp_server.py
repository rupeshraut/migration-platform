# PIT Report Parser
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def _parse_pit_xml_report(report_path: str) -> list[MutationReport]:
    """Parse PIT XML mutations report into structured data."""
    reports = {}

    try:
        tree = ET.parse(report_path)
        root = tree.getroot()
    except Exception as e:
        return []

    for mutation in root.findall(".//mutation"):
        detected = mutation.attrib.get("detected", "false") == "true"
        status = mutation.attrib.get("status", "NO_COVERAGE")
        clazz = mutation.findtext("mutatedClass", "")
        method = mutation.findtext("mutatedMethod", "")
        line = int(mutation.findtext("lineNumber", "0"))
        mutator = mutation.findtext("mutator", "")
        description = mutation.findtext("description", "")
        source_file = mutation.findtext("sourceFile", "")
        killing_test = mutation.findtext("killingTest", "")

        if clazz not in reports:
            reports[clazz] = MutationReport(class_fqcn=clazz, source_file=source_file)

        r = reports[clazz]
        r.total_mutants += 1

        if status == "KILLED":
            r.killed += 1
        elif status == "SURVIVED":
            r.survived += 1
            r.surviving_mutants.append({
                "method": method,
                "line": line,
                "mutator": _simplify_mutator(mutator),
                "description": description,
                "status": status,
            })
        elif status == "NO_COVERAGE":
            r.no_coverage += 1
            r.surviving_mutants.append({
                "method": method,
                "line": line,
                "mutator": _simplify_mutator(mutator),
                "description": description,
                "status": "NO_COVERAGE",
            })
        elif status == "TIMED_OUT":
            r.timed_out += 1

    # Compute scores
    for r in reports.values():
        denominator = r.killed + r.survived
        r.mutation_score = round((r.killed / denominator * 100) if denominator > 0 else 0.0, 1)
        total_coverable = r.total_mutants - r.timed_out
        r.line_coverage = round(
            ((r.killed + r.timed_out) / r.total_mutants * 100) if r.total_mutants > 0 else 0.0, 1
        )

        # Aggregate by method
        method_stats = defaultdict(lambda: {"killed": 0, "survived": 0, "no_coverage": 0})
        for mut in r.surviving_mutants:
            m = mut["method"]
            method_stats[m][mut["status"].lower().replace(" ", "_")] += 1

        r.methods = [
            {"method": m, **stats}
            for m, stats in method_stats.items()
        ]

    return list(reports.values())


def _simplify_mutator(mutator_fqcn: str) -> str:
    """Simplify PIT mutator names to readable form."""
    mapping = {
        "ConditionalsBoundaryMutator": "BOUNDARY (< → <=, > → >=)",
        "IncrementsMutator": "INCREMENT (++ → --, -- → ++)",
        "InvertNegsMutator": "NEGATE (- → +)",
        "MathMutator": "MATH (+→-, *→/, etc.)",
        "NegateConditionalsMutator": "NEGATE_COND (== → !=, < → >=)",
        "ReturnValsMutator": "RETURN_VALUE (return X → null/0/false)",
        "VoidMethodCallMutator": "VOID_CALL (remove void method call)",
        "EmptyObjectReturnValsMutator": "EMPTY_RETURN (return empty instead of value)",
        "FalseReturnValsMutator": "FALSE_RETURN (return false)",
        "TrueReturnValsMutator": "TRUE_RETURN (return true)",
        "NullReturnValsMutator": "NULL_RETURN (return null)",
        "PrimitiveReturnsMutator": "PRIMITIVE_RETURN (return 0)",
    }
    simple = mutator_fqcn.rsplit(".", 1)[-1] if "." in mutator_fqcn else mutator_fqcn
    return mapping.get(simple, simple)


def _find_pit_report(project_path: str) -> Optional[str]:
    """Find the most recent PIT mutations.xml in a project."""
    candidates = [
        os.path.join(project_path, "target", "pit-reports"),
        os.path.join(project_path, "build", "reports", "pitest"),
    ]
    for base in candidates:
        if not os.path.isdir(base):
            continue
        # PIT creates timestamped subdirectories
        subdirs = sorted(Path(base).iterdir(), reverse=True)
        for sd in subdirs:
            xml = sd / "mutations.xml"
            if xml.is_file():
                return str(xml)
    return None


