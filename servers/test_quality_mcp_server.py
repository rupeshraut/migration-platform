# Test Generation
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def _generate_test_class(
    class_fqcn: str,
    gaps: list[TestGap],
    source_path: str,
    config: dict,
) -> str:
    """Generate a meaningful test class targeting specific mutation gaps."""
    simple_class = class_fqcn.rsplit(".", 1)[-1]
    package = class_fqcn.rsplit(".", 1)[0] if "." in class_fqcn else ""
    use_assertj = config.get("use_assertj", True)
    use_mockito = config.get("use_mockito", True)
    max_per_method = config.get("max_tests_per_method", 5)

    # Read source for dependency analysis
    source_content = ""
    if source_path and os.path.isfile(source_path):
        try:
            source_content = Path(source_path).read_text(errors="ignore")
        except Exception:
            pass

    # Detect dependencies for mocking
    deps = []
    if source_content:
        for dep_match in re.finditer(
            r"private\s+final\s+([\w<>]+)\s+(\w+)\s*;", source_content
        ):
            dep_type = dep_match.group(1)
            dep_name = dep_match.group(2)
            if dep_type[0].isupper() and dep_type not in (
                "String", "Integer", "Long", "Boolean", "List", "Map", "Set",
                "Logger", "ObjectMapper",
            ):
                deps.append({"type": dep_type, "name": dep_name})

    # Build test class
    lines = []
    lines.append(f"package {package};\n" if package else "")
    lines.append("")

    # Imports
    lines.append("import org.junit.jupiter.api.BeforeEach;")
    lines.append("import org.junit.jupiter.api.Test;")
    lines.append("import org.junit.jupiter.api.DisplayName;")
    lines.append("import org.junit.jupiter.api.Nested;")
    lines.append("import org.junit.jupiter.params.ParameterizedTest;")
    lines.append("import org.junit.jupiter.params.provider.ValueSource;")
    lines.append("import org.junit.jupiter.params.provider.NullAndEmptySource;")

    if use_assertj:
        lines.append("import static org.assertj.core.api.Assertions.assertThat;")
        lines.append("import static org.assertj.core.api.Assertions.assertThatThrownBy;")
    else:
        lines.append("import static org.junit.jupiter.api.Assertions.*;")

    if use_mockito and deps:
        lines.append("import org.junit.jupiter.api.extension.ExtendWith;")
        lines.append("import org.mockito.InjectMocks;")
        lines.append("import org.mockito.Mock;")
        lines.append("import org.mockito.junit.jupiter.MockitoExtension;")
        lines.append("import static org.mockito.Mockito.*;")
        lines.append("import static org.mockito.ArgumentMatchers.*;")

    lines.append("")
    lines.append(f"/**")
    lines.append(f" * Mutation-driven tests for {simple_class}.")
    lines.append(f" *")
    lines.append(f" * Generated to kill {len(gaps)} surviving mutants.")
    lines.append(f" * Each test targets a specific mutation gap identified by PIT.")
    lines.append(f" *")
    lines.append(f" * @see {class_fqcn}")
    lines.append(f" */")

    if use_mockito and deps:
        lines.append(f"@ExtendWith(MockitoExtension.class)")
    lines.append(f"class {simple_class}MutationTest {{")
    lines.append("")

    # Mocks
    for dep in deps:
        lines.append(f"    @Mock")
        lines.append(f"    private {dep['type']} {dep['name']};")
        lines.append("")

    if use_mockito and deps:
        lines.append(f"    @InjectMocks")
    lines.append(f"    private {simple_class} underTest;")
    lines.append("")

    # Setup
    if not deps:
        lines.append("    @BeforeEach")
        lines.append("    void setUp() {")
        lines.append(f"        underTest = new {simple_class}();")
        lines.append("    }")
        lines.append("")

    # Group gaps by method
    by_method: dict[str, list[TestGap]] = defaultdict(list)
    for gap in gaps:
        by_method[gap.method_name].append(gap)

    for method_name, method_gaps in by_method.items():
        if method_name in ("<init>", "<clinit>"):
            continue

        display_method = method_name[0].upper() + method_name[1:]
        lines.append(f"    @Nested")
        lines.append(f'    @DisplayName("{method_name}()")')
        lines.append(f"    class {display_method}Tests {{")
        lines.append("")

        for i, gap in enumerate(method_gaps[:max_per_method]):
            test_name = _generate_test_name(gap, i)
            lines.append(f"        @Test")
            lines.append(f'        @DisplayName("{_escape_java_string(gap.description)}")')
            lines.append(f"        void {test_name}() {{")
            lines.append(f"            // Target: {gap.mutant_type} at line {gap.line_number}")
            lines.append(f"            // {gap.suggested_test[:100]}")
            lines.append(f"")
            lines.append(f"            // Arrange")
            lines.append(f"            // TODO: Set up test data that makes the mutation detectable")
            lines.append(f"")
            lines.append(f"            // Act")
            lines.append(f"            // var result = underTest.{method_name}(...);")
            lines.append(f"")
            lines.append(f"            // Assert — MUST verify the specific behavior affected by the mutant")

            # Generate assertion hints based on mutant type
            if "BOUNDARY" in gap.mutant_type:
                lines.append(f"            // Use EXACT boundary values: if code uses <, test with ==")
                lines.append(f"            // assertThat(result).isEqualTo(expectedBoundaryResult);")
            elif "RETURN_VALUE" in gap.mutant_type or "NULL_RETURN" in gap.mutant_type:
                lines.append(f"            // assertThat(result).isNotNull();")
                lines.append(f"            // assertThat(result).isEqualTo(expectedValue);")
            elif "VOID_CALL" in gap.mutant_type:
                lines.append(f"            // verify(mockDependency).expectedMethodCall(any());")
                lines.append(f"            // verify(mockDependency, times(1)).sideEffect(...);")
            elif "NEGATE_COND" in gap.mutant_type:
                lines.append(f"            // Test BOTH branches of the condition at line {gap.line_number}")
                lines.append(f"            // assertThat(resultForTrue).isNotEqualTo(resultForFalse);")
            elif "MATH" in gap.mutant_type:
                lines.append(f"            // Use values where +, -, *, / produce DIFFERENT results")
                lines.append(f"            // Avoid: 0, 1, 2 (too symmetric). Use: 3, 7, 13")
            else:
                lines.append(f"            // assertThat(result).satisfies(r -> {{ /* verify behavior */ }});")

            lines.append(f"        }}")
            lines.append("")

        lines.append(f"    }}")
        lines.append("")

    lines.append("}")
    return "\n".join(lines)


def _generate_test_name(gap: TestGap, index: int) -> str:
    """Generate a descriptive test method name."""
    method = gap.method_name
    mutant = gap.mutant_type.split("(")[0].strip()

    name_parts = [
        "should",
        "Detect",
        re.sub(r"[^a-zA-Z0-9]", "", mutant).title(),
        "MutationIn",
        method[0].upper() + method[1:] if method else "Method",
    ]
    if index > 0:
        name_parts.append(f"Case{index + 1}")
    name_parts.append(f"Line{gap.line_number}")

    return "".join(name_parts)


def _escape_java_string(s: str) -> str:
    """Escape a string for use in Java string literal."""
    return s.replace('"', '\\"').replace("\n", " ")[:120]


