def _run_pit(project_path: str, target_class: str = "", config: dict = None) -> dict:
    """Execute PIT mutation testing."""
    config = config or _load_config()
    project_path = os.path.abspath(project_path)

    # Detect build tool
    mvnw = os.path.join(project_path, "mvnw")
    mvn = mvnw if os.path.isfile(mvnw) else "mvn"

    cmd = [
        mvn,
        "org.pitest:pitest-maven:mutationCoverage",
        f"-Dpit.mutators={config.get('pit_mutators', 'STRONGER')}",
        f"-Dpit.threads={config.get('pit_threads', 4)}",
        f"-Dpit.timeoutFactor={config.get('pit_timeout_factor', 1.25)}",
        f"-Dpit.timeoutConstant={config.get('pit_timeout_constant', 3000)}",
        "-Dpit.outputFormats=XML",
    ]

    if target_class:
        cmd.append(f"-Dpit.targetClasses={target_class}")

    env = os.environ.copy()
    java_home = os.environ.get("JAVA_HOME", "")
    if java_home:
        env["PATH"] = os.path.join(java_home, "bin") + os.pathsep + env.get("PATH", "")

    try:
        result = subprocess.run(
            cmd, cwd=project_path, capture_output=True, text=True,
            timeout=900, env=env,
        )
        return {
            "success": result.returncode == 0,
            "stdout": result.stdout[-3000:],
            "stderr": result.stderr[-2000:],
        }
    except subprocess.TimeoutExpired:
        return {"success": False, "stderr": "PIT timed out after 15 minutes"}
    except FileNotFoundError as e:
        return {"success": False, "stderr": str(e)}


