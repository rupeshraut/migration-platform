# Risk Scoring
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━


def _score_class_risk(class_fqcn: str, source_path: str, config: dict) -> RiskScore:
    """Score a class by business risk based on its name, content, and patterns."""
    risk = RiskScore(class_fqcn=class_fqcn)
    weights = config.get("risk_weights", {})

    # Read source if available
    content = ""
    if source_path and os.path.isfile(source_path):
        try:
            content = Path(source_path).read_text(errors="ignore").lower()
        except Exception:
            pass

    fqcn_lower = class_fqcn.lower()
    searchable = fqcn_lower + " " + content

    # Financial risk
    financial_indicators = config.get("financial_indicators", [])
    fin_matches = [ind for ind in financial_indicators if ind.lower() in searchable]
    if fin_matches:
        w = weights.get("financial", 3)
        risk.risk_factors.append({
            "factor": "FINANCIAL",
            "weight": w,
            "indicators": fin_matches[:5],
            "reason": "Handles money, pricing, or financial calculations",
        })
        risk.overall_risk += w * len(fin_matches)

    # State transition risk
    state_indicators = config.get("state_indicators", [])
    state_matches = [ind for ind in state_indicators if ind.lower() in searchable]
    if state_matches:
        w = weights.get("state_transition", 2)
        risk.risk_factors.append({
            "factor": "STATE_TRANSITION",
            "weight": w,
            "indicators": state_matches[:5],
            "reason": "Manages state transitions or workflow",
        })
        risk.overall_risk += w * len(state_matches)

    # Security risk
    security_indicators = config.get("security_indicators", [])
    sec_matches = [ind for ind in security_indicators if ind.lower() in searchable]
    if sec_matches:
        w = weights.get("security", 3)
        risk.risk_factors.append({
            "factor": "SECURITY",
            "weight": w,
            "indicators": sec_matches[:5],
            "reason": "Handles authentication, authorization, or sensitive data",
        })
        risk.overall_risk += w * len(sec_matches)

    # Complexity indicators from code
    if content:
        # Conditional complexity
        conditionals = len(re.findall(r"\bif\b|\bswitch\b|\b\?\s*:", content))
        if conditionals > 5:
            w = weights.get("conditional_logic", 1)
            risk.risk_factors.append({
                "factor": "COMPLEX_CONDITIONALS",
                "weight": w,
                "count": conditionals,
                "reason": f"{conditionals} conditional branches — high bug potential",
            })
            risk.overall_risk += w * min(conditionals, 10)

        # Error handling
        try_blocks = len(re.findall(r"\btry\s*\{", content))
        if try_blocks > 2:
            w = weights.get("error_handling", 2)
            risk.risk_factors.append({
                "factor": "ERROR_HANDLING",
                "weight": w,
                "count": try_blocks,
                "reason": f"{try_blocks} try-catch blocks — error paths need testing",
            })
            risk.overall_risk += w * min(try_blocks, 5)

        # Data mutation (DB writes, external calls)
        mutations = len(re.findall(
            r"\b(save|update|delete|insert|persist|merge|remove|execute|send|publish)\b", content
        ))
        if mutations > 0:
            w = weights.get("data_mutation", 2)
            risk.risk_factors.append({
                "factor": "DATA_MUTATION",
                "weight": w,
                "count": mutations,
                "reason": f"{mutations} data-mutating operations",
            })
            risk.overall_risk += w * min(mutations, 8)

    # Categorize
    if risk.overall_risk >= 15:
        risk.category = "CRITICAL"
    elif risk.overall_risk >= 8:
        risk.category = "HIGH"
    elif risk.overall_risk >= 4:
        risk.category = "MEDIUM"
    else:
        risk.category = "LOW"

    risk.overall_risk = round(risk.overall_risk, 1)
    return risk


