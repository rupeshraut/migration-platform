@dataclass
class MutationReport:
    """Parsed PIT mutation testing report for a single class."""
    class_fqcn: str
    source_file: str = ""
    total_mutants: int = 0
    killed: int = 0
    survived: int = 0
    no_coverage: int = 0
    timed_out: int = 0
    mutation_score: float = 0.0       # killed / (killed + survived) * 100
    line_coverage: float = 0.0
    methods: list[dict] = field(default_factory=list)
    surviving_mutants: list[dict] = field(default_factory=list)


@dataclass
class RiskScore:
    """Business risk assessment for a class."""
    class_fqcn: str
    overall_risk: float = 0.0
    risk_factors: list[dict] = field(default_factory=list)
    category: str = "LOW"             # LOW, MEDIUM, HIGH, CRITICAL


@dataclass
class TestGap:
    """A specific test gap — a method or branch that needs better tests."""
    class_fqcn: str
    method_name: str
    gap_type: str                     # SURVIVING_MUTANT, NO_COVERAGE, WEAK_ASSERTION
    description: str = ""
    mutant_type: str = ""             # CONDITIONALS_BOUNDARY, RETURN_VALUES, etc.
    line_number: int = 0
    risk_score: float = 0.0
    suggested_test: str = ""


